
const express = require('express');
const authMiddleware = require('../middlewares');
const { handleGetTasksByBoard, handleCreateTask, handleUpdateTask, handleDeleteTask } = require('../controller/taskController');
const router = express.Router();


/**
 * @swagger
 * /api/tasks/{boardId}:
 *   get:
 *     summary: Get all tasks for a specific board
 *     tags: [Tasks]
 *     parameters:
 *       - in: path
 *         name: boardId
 *         required: true
 *         schema:
 *           type: string
 *     responses:
 *       200:
 *         description: List of tasks
 */

/**
 * @swagger
 * /api/tasks:
 *   post:
 *     summary: Create a new task
 *     tags: [Tasks]
 *     requestBody:
 *       required: true
 *       content:
 *         application/json:
 *           schema:
 *             type: object
 *             properties:
 *               title:
 *                 type: string
 *               description:
 *                 type: string
 *               status:
 *                 type: string
 *                 enum: [todo, in-progress, done]
 *               boardId:
 *                 type: string
 *     responses:
 *       201:
 *         description: Task created
 */


/**
 * @swagger
 * /api/tasks/{id}:
 *   put:
 *     summary: Update a task by ID
 *     tags: [Tasks]
 *     parameters:
 *       - in: path
 *         name: id
 *         required: true
 *         schema:
 *           type: string
 *     requestBody:
 *       required: true
 *       content:
 *         application/json:
 *           schema:
 *             type: object
 *             properties:
 *               title:
 *                 type: string
 *               description:
 *                 type: string
 *               status:
 *                 type: string
 *                 enum: [todo, in-progress, done]
 *     responses:
 *       200:
 *         description: Task updated
 */

/**
 * @swagger
 * /api/tasks/{id}:
 *   delete:
 *     summary: Delete a task by ID
 *     tags: [Tasks]
 *     parameters:
 *       - in: path
 *         name: id
 *         required: true
 *         schema:
 *           type: string
 *     responses:
 *       200:
 *         description: Task deleted
 */


router.get('/task/:boardId', authMiddleware, handleGetTasksByBoard);

router.post('/task', authMiddleware, handleCreateTask);

router.put('/task/:id', authMiddleware, handleUpdateTask);

router.delete('/task/:id', authMiddleware, handleDeleteTask);

module.exports = router;
